<script>
    function parsePopUpOrderProduct(mode, data){
        let modal = document.getElementById('modal-order-product');
        let title = document.getElementById('title-order-product');
        let name = document.getElementById('name');
        let price = document.getElementById('price');
        let category = document.getElementById('category');
        let description = document.getElementById('description');
        if(mode == 'add'){
            title.innerHTML = 'Thêm sản phẩm';
            name.value = '';
            price.value = '';
            category.value = '';
            description.value = '';
        }else if(mode == 'edit'){
            title.innerHTML = 'Edit product';
            name.value = data.name;
            price.value = data.price;
            category.value = data.category;
            description.value = data.description;
        }
        modal.classList.remove('hidden');
    }
</script>